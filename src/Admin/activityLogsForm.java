    public void logActivity(String action,  int eventId) {
   Session sess = Session.getInstance();
    int userId = sess.getUid();

    String sql = "INSERT INTO tbl_logs (u_id, e_id, actions, created_at) " +
                 "VALUES (" + userId + ", " + eventId + ", '" + action.replace("'", "''") + "', NOW())";

    dbConnector dbc = new dbConnector();
    boolean success = dbc.insertData(sql);

    if (!success) {
        System.out.println("Failed to log activity.");
    }


}
 
    
