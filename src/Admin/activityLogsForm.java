    private void atableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_atableMouseClicked
       int rowIndex= atable.getSelectedRow();
        
        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null,"Please Select an Item!");
        }else{
            try{
            dbConnector dbc= new dbConnector();
            TableModel tbl= atable.getModel();
            ResultSet rs = dbc.getData("SELECT*FROM tbl_logs WHERE log_id ='"+tbl.getValueAt(rowIndex, 0)+"'");
            if(rs.next()){
            lid.setText(""+rs.getInt("log_id"));
            uid.setText(""+rs.getString("u_id"));
            eid.setText(""+rs.getString("e_id"));
            ld.setText(""+rs.getString("created_at"));
            lag.setText(""+rs.getString("actions"));
            }
        }catch(SQLException ex){
                System.out.println(""+ex);
        }
            
        }       
                
         TableModel tbl= atable.getModel();
    }//GEN-LAST:event_atableMouseClicked

