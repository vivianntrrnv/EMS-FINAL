    private void etableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_etableMouseClicked
       int rowIndex= etable.getSelectedRow();
        
        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null,"Please Select an Item!");
        }else{
            try{
            dbConnector dbc= new dbConnector();
            TableModel tbl= etable.getModel();
            ResultSet rs = dbc.getData("SELECT*FROM tbl_events WHERE e_id ='"+tbl.getValueAt(rowIndex, 0)+"'");
            if(rs.next()){
            eid.setText(""+rs.getInt("e_id"));
            en.setText(""+rs.getString("e_name"));
            el.setText(""+rs.getString("e_location"));
            ec.setText(""+rs.getString("e_capacity"));
            ep.setText(""+rs.getString("e_price"));
            es.setSelectedItem(""+rs.getString("e_status"));
            p_add.setEnabled(false);
            addlabel.setForeground(red);
            checkadd= false;
             }
        }catch(SQLException ex){
                System.out.println(""+ex);
        }
            
        }       
                
         TableModel tbl= etable.getModel();
    }//GEN-LAST:event_etableMouseClicked

