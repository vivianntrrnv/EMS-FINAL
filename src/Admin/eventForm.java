    private void p_addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_p_addMouseClicked
        if(checkadd){
            
            if(en.getText().isEmpty()|| ep.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"All fields are required!");
            
            }else{
                  dbConnector dbc= new dbConnector();
                    dbc.insertData("INSERT INTO tbl_events(e_name, e_price, e_status,e_location,e_capacity,e_image)VALUES('"+en.getText()+"', '"+ep.getText() +"', '"+es.getSelectedItem()+"','"+el.getText()+"','"+ec.getText()+"','')");
                     JOptionPane.showMessageDialog(null,"Successfully Added!");
                     displayData();
                     
                     checkadd= true;
                   addlabel.setForeground(white);
                   eid.setText("");
                   en.setText("");
                   el.setText("");
                   ec.setText("");
                   ep.setText("");
                   es.setSelectedIndex(0);
                    }
               
              
        }else{
            JOptionPane.showMessageDialog(null,"Clear the Field First");
         }
        
    }//GEN-LAST:event_p_addMouseClicked

